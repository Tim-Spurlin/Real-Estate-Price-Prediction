import pandas as pd
import numpy as np
import time

from sklearn.pipeline import make_pipeline
from sklearn.preprocessing import RobustScaler
from sklearn.linear_model import Lasso, Ridge
from sklearn.ensemble import StackingRegressor, GradientBoostingRegressor
from xgboost import XGBRegressor
from lightgbm import LGBMRegressor
from catboost import CatBoostRegressor

from src.preprocessing import preprocess_data

def train_and_submit():
    print("Starting Advanced Machine Learning Pipeline...")
    start_time = time.time()
    
    # Execute massive feature synthesis and Boruta reduction
    X_train, y_train, X_test, test_ids = preprocess_data()
    print(f"Algorithm Ready. X_train Matrix Optimized to: {X_train.shape}")
    
    # 1. Base Models Selection (Re-calibrated for highly dimensional space)
    
    # Robust linear models handling multi-collinearity generated by DFS
    lasso = make_pipeline(RobustScaler(), Lasso(alpha=0.005, random_state=42))
    ridge = make_pipeline(RobustScaler(), Ridge(alpha=20.0, random_state=42))

    # Boosters aggressively constrained against thousands of synthetic branches
    xgb = XGBRegressor(
        learning_rate=0.01, n_estimators=1000,
        max_depth=4, min_child_weight=1,
        gamma=0.05, subsample=0.8,
        colsample_bytree=0.2, # Constrain to 20% to battle dimensionality
        objective='reg:squarederror', nthread=-1,
        seed=42, random_state=42
    )

    lgbm = LGBMRegressor(
        objective='regression', num_leaves=6,
        learning_rate=0.03, n_estimators=800,
        feature_fraction=0.25, # High constraint
        min_data_in_leaf=10,
        verbose=-1, random_state=42
    )

    catb = CatBoostRegressor(
        iterations=900, learning_rate=0.03,
        depth=5, l2_leaf_reg=10, # Heavy L2
        eval_metric='RMSE',
        random_seed=42, logging_level='Silent'
    )
    
    gbr = GradientBoostingRegressor(
        n_estimators=1000, learning_rate=0.05,
        max_depth=4, max_features='sqrt',
        min_samples_leaf=15, min_samples_split=10, 
        loss='huber', random_state=42
    )

    # 2. Stacking Regressor utilizing Ridge as the dominant meta-anchor
    estimators = [('lasso', lasso), ('ridge', ridge), ('xgb', xgb), ('lgbm', lgbm), ('catb', catb), ('gbr', gbr)]
    
    stack = StackingRegressor(
        estimators=estimators,
        final_estimator=make_pipeline(RobustScaler(), Ridge(alpha=15.0)),
        cv=4,
        n_jobs=-1
    )

    print("Training Massive Stacking Core (This may take significant time)...")
    stack.fit(X_train, y_train)
    print("Training absolutely complete!")

    print("Executing Submissions...")
    preds_log = stack.predict(X_test)
    preds = np.expm1(preds_log)

    submission = pd.DataFrame({'Id': test_ids, 'SalePrice': preds})
    
    submission_path = "submission.csv"
    submission.to_csv(submission_path, index=False)
    print(f"Top-Tier Submission saved to {submission_path}")
    print(f"System elapsed time: {time.time() - start_time:.2f} seconds")

if __name__ == "__main__":
    train_and_submit()
